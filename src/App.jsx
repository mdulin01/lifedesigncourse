import React, { useState, useEffect } from 'react';
import { Routes, Route } from 'react-router-dom';
import { auth, googleProvider } from './firebase-config';
import { signInWithPopup, onAuthStateChanged, signOut } from 'firebase/auth';
import { allowedEmails } from './constants';
import { BTSProvider } from './contexts/BTSContext';
import CourseLanding from './components/CourseLanding';
import Portal from './components/Portal';

export default function App() {
  const [user, setUser] = useState(null);
  const [authLoading, setAuthLoading] = useState(true);
  const [authError, setAuthError] = useState('');

  useEffect(() => {
    const unsub = onAuthStateChanged(auth, (u) => {
      if (u && allowedEmails.includes(u.email?.toLowerCase())) {
        setUser(u);
      } else {
        setUser(null);
        if (u) signOut(auth);
      }
      setAuthLoading(false);
    });
    return unsub;
  }, []);

  const [deniedEmail, setDeniedEmail] = useState('');

  const handleSignIn = async () => {
    try {
      setAuthError('');
      setDeniedEmail('');
      const result = await signInWithPopup(auth, googleProvider);
      if (!allowedEmails.includes(result.user.email?.toLowerCase())) {
        setDeniedEmail(result.user.email || '');
        await signOut(auth);
        setAuthError('denied');
      }
    } catch (e) {
      if (e.code !== 'auth/popup-closed-by-user') {
        setAuthError('Sign-in failed: ' + e.message);
      }
    }
  };

  const handleSignOut = async () => {
    await signOut(auth);
    setUser(null);
  };

  if (authLoading) {
    return (
      <div className="min-h-screen bg-slate-950 flex items-center justify-center">
        <div className="w-8 h-8 border-2 border-emerald-400 border-t-transparent rounded-full animate-spin" />
      </div>
    );
  }

  return (
    <BTSProvider>
      <Routes>
        <Route path="/portal/*" element={
          user ? (
            <Portal user={user} onSignOut={handleSignOut} />
          ) : (
            <div className="min-h-screen bg-slate-950 flex items-center justify-center px-4">
              <div className="w-full max-w-sm text-center">
                <div className="mb-8">
                  <div className="text-5xl mb-4">ü§ñ</div>
                  <h1 className="text-2xl font-bold text-white mb-1">AI Life Design Portal</h1>
                  <p className="text-white/50 text-sm">Sign in to access your tools</p>
                </div>
                <button
                  onClick={handleSignIn}
                  className="w-full flex items-center justify-center gap-3 px-6 py-3 bg-white hover:bg-gray-100 text-gray-800 font-medium rounded-xl transition active:scale-[0.98] shadow-lg"
                >
                  <svg className="w-5 h-5" viewBox="0 0 24 24"><path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4"/><path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/><path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/><path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/></svg>
                  Sign in with Google
                </button>
                {authError === 'denied' ? (
                  <div className="mt-6 bg-white/[0.03] border border-white/10 rounded-xl p-5 text-left">
                    <p className="text-white/70 text-sm mb-3">
                      <span className="font-semibold text-white">{deniedEmail}</span> doesn't have portal access yet.
                    </p>
                    <a
                      href={`mailto:mdulin@gmail.com?subject=${encodeURIComponent('Portal Access Request ‚Äî AI Life Design')}&body=${encodeURIComponent(`Hi Mike,\n\nI'd like to request access to the AI Life Design Course portal.\n\nEmail: ${deniedEmail}\n\nThank you!`)}`}
                      className="w-full flex items-center justify-center gap-2 px-4 py-2.5 bg-emerald-500/15 border border-emerald-500/25 text-emerald-400 text-sm font-medium rounded-xl hover:bg-emerald-500/25 transition"
                    >
                      Request Access
                    </a>
                    <button
                      onClick={() => { setAuthError(''); setDeniedEmail(''); }}
                      className="mt-3 text-white/30 hover:text-white/50 text-xs transition w-full text-center"
                    >
                      Try a different account
                    </button>
                  </div>
                ) : authError ? (
                  <p className="mt-4 text-red-400 text-sm">{authError}</p>
                ) : null}
                <a href="/" className="block mt-6 text-emerald-400/60 hover:text-emerald-400 text-sm transition">
                  ‚Üê Back to course
                </a>
              </div>
            </div>
          )
        } />
        <Route path="*" element={
          <CourseLanding onSignIn={handleSignIn} user={user} onSignOut={handleSignOut} />
        } />
      </Routes>
    </BTSProvider>
  );
}
